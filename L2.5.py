# Escape characters and types of quotes
print('It\'s a beautiful day')
print('We are from "IIT" madras')
print('My name is Sajal. I am from Karnal.')
print('My name is Sajal.\tI am from Karnal.')
print('My name is Sajal.\nI am from Karnal.')

x = 'it is a string'
y = "it is also a string"
z = '''first line
second line
third line
fourth line'''
print(x)
print(y)
print(z)

